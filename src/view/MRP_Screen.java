    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
        try {
            ResultSet result = new DBSearch().get_Material_Details_According_to_OrderId(this.orderID.getSelectedItem().toString());

            while (result.next()) {
                MRPController.insert_data_to_Order_row_material(this.orderID.getSelectedItem().toString(), result.getString(2), result.getString(5), "");
            }

            JOptionPane.showMessageDialog(this, "Details has been added!!");
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_saveBTNActionPerformed

