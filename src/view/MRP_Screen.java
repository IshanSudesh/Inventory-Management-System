    private void loadingOrderIDs() {
        try {
            ResultSet result = new DBSearch().getOrderIDs();
            while (result.next()) {
                String nextOrderID = result.getString(1);
                this.orderID.addItem(nextOrderID);
            }
            DBConnection.closeCon();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }


