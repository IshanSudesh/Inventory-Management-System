    private void setMaterialDetails(String order_id) {
        try {
            ResultSet result = new DBSearch().get_Material_Details_According_to_OrderId(order_id);
            DefaultTableModel model = (DefaultTableModel) material_table.getModel();
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            while (result.next()) {
                Object[] row = {result.getString(2), result.getString(3), result.getString(5)};
//have to add details to the database
                model.addRow(row);
            }

            DBConnection.closeCon();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

//         set material plan //all details
    private void setFullMaterialDetails(String order_id) {
        try {
            ResultSet result = new DBSearch().get_Material_Details_According_to_OrderId(order_id);
            DefaultTableModel model = (DefaultTableModel) material_table.getModel();
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            while (result.next()) {
                Object[] row = {result.getString(3), result.getString(4), result.getString(9)};

                model.addRow(row);
            }

            DBConnection.closeCon();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

    //set customer details and order date
    private void setCustomerdetails(String order_id) {
        try {
            ResultSet result = new DBSearch().get_Customer_Details_From_OrderId(order_id);
            while (result.next()) {
                this.custName.setText(result.getString(4));
                this.order_date_field.setText(result.getString(2));
            }
            DBConnection.closeCon();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }

