    private void loadingMaterialId(){
        try {
            ResultSet result = new DBSearch().getMaterialDetails();
            while(result.next()){
                String nextOrderID = result.getString(1);
                this.material_IdCMB.addItem(nextOrderID);
            }
            DBConnection.closeCon();
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
