    private void material_IdCMBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_material_IdCMBActionPerformed
        clearFeild();
        material_id = this.material_IdCMB.getSelectedItem().toString();
        material_name();
    }//GEN-LAST:event_material_IdCMBActionPerformed

    public void clearFeild(){
        this.qnty.setText("");
    }
    
    private void material_name(){
        
        try {
           ResultSet result =  new DBSearch().check_Availability_Material_Stock(material_id);
           while(result.next()){
               this.materialName.setText(result.getString(2));
               material_Stock_qty = Double.parseDouble(result.getString(3));
           }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       
        boolean isSuccess = new WarehouseController().changing_Material_Stock(material_id, Double.parseDouble(this.qnty.getText().toString()) + material_Stock_qty);
       
        if(isSuccess){
            JOptionPane.showMessageDialog(null, "Stock has been Changed!!", "Sucessfull", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "Materials are not available", "Failed", JOptionPane.INFORMATION_MESSAGE);
        }
        clearFeild();
    }//GEN-LAST:event_saveActionPerformed

