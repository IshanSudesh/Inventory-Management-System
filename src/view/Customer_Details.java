    private void customer_details_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customer_details_tableMouseClicked
       
        int rowIndex = this.customer_details_table.getSelectedRow();
        TableModel tableModel = this.customer_details_table.getModel();
        
        cusId = tableModel.getValueAt(rowIndex, 0).toString();
        
        this.cus_id.setText(cusId);
        this.cus_name.setText(tableModel.getValueAt(rowIndex, 1).toString());
        this.cus_address.setText(tableModel.getValueAt(rowIndex, 2).toString());
        this.cus_phone.setText(tableModel.getValueAt(rowIndex, 3).toString());
        this.cus_email.setText(tableModel.getValueAt(rowIndex, 4).toString());
    }//GEN-LAST:event_customer_details_tableMouseClicked

