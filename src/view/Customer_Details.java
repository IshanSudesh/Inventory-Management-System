   private void addCustomerDetails(){
       List<Customer> customer = new CustomerController().allCustomerDetails();
       
       DefaultTableModel tableModel = (DefaultTableModel)this.customer_details_table.getModel();
       while (tableModel.getRowCount() > 0) {
            tableModel.removeRow(0);
       }
       for(int a=0; a<customer.size();a++){
            String cusData[] = {String.valueOf(customer.get(a).getId()), customer.get(a).getName(), customer.get(a).getAddress(), customer.get(a).getPhone(), customer.get(a).getEmail()};
            tableModel.addRow(cusData);
       }
       
   }
   
